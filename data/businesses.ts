export function getBusinessBySlug(slug: string): Business | undefined {
  return businesses.find((b) => b.slug === slug)
}

export function getBusinessesByCategory(categorySlug: string): Business[] {
  return businesses.filter((b) => b.categorySlug === categorySlug)
}

export function searchBusinesses(query: string): Business[] {
  const lower = query.toLowerCase()
  return businesses.filter(
    (b) =>
      b.name.toLowerCase().includes(lower) ||
      b.overview.toLowerCase().includes(lower) ||
      b.tags.some((t) => t.toLowerCase().includes(lower))
  )
}

export function filterBusinesses(filters: {
  category?: string
  investmentLevel?: string
  profitRange?: string
  search?: string
}): Business[] {
  let results = [...businesses]

  if (filters.search) {
    const lower = filters.search.toLowerCase()
    results = results.filter(
      (b) =>
        b.name.toLowerCase().includes(lower) ||
        b.overview.toLowerCase().includes(lower) ||
        b.tags.some((t) => t.toLowerCase().includes(lower))
    )
  }

  if (filters.category) {
    results = results.filter((b) => b.categorySlug === filters.category)
  }

  if (filters.investmentLevel) {
    results = results.filter((b) => b.investmentLevel === filters.investmentLevel)
  }

  if (filters.profitRange) {
    results = results.filter((b) => b.profitRange === filters.profitRange)
  }

  return results
}
